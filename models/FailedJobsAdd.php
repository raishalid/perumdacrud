    // Validate form
    protected function validateForm()
    {
        global $Language, $Security;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        $validateForm = true;
        if ($this->uuid->Visible && $this->uuid->Required) {
            if (!$this->uuid->IsDetailKey && EmptyValue($this->uuid->FormValue)) {
                $this->uuid->addErrorMessage(str_replace("%s", $this->uuid->caption(), $this->uuid->RequiredErrorMessage));
            }
        }
        if ($this->connection->Visible && $this->connection->Required) {
            if (!$this->connection->IsDetailKey && EmptyValue($this->connection->FormValue)) {
                $this->connection->addErrorMessage(str_replace("%s", $this->connection->caption(), $this->connection->RequiredErrorMessage));
            }
        }
        if ($this->queue->Visible && $this->queue->Required) {
            if (!$this->queue->IsDetailKey && EmptyValue($this->queue->FormValue)) {
                $this->queue->addErrorMessage(str_replace("%s", $this->queue->caption(), $this->queue->RequiredErrorMessage));
            }
        }
        if ($this->payload->Visible && $this->payload->Required) {
            if (!$this->payload->IsDetailKey && EmptyValue($this->payload->FormValue)) {
                $this->payload->addErrorMessage(str_replace("%s", $this->payload->caption(), $this->payload->RequiredErrorMessage));
            }
        }
        if ($this->exception->Visible && $this->exception->Required) {
            if (!$this->exception->IsDetailKey && EmptyValue($this->exception->FormValue)) {
                $this->exception->addErrorMessage(str_replace("%s", $this->exception->caption(), $this->exception->RequiredErrorMessage));
            }
        }
        if ($this->failed_at->Visible && $this->failed_at->Required) {
            if (!$this->failed_at->IsDetailKey && EmptyValue($this->failed_at->FormValue)) {
                $this->failed_at->addErrorMessage(str_replace("%s", $this->failed_at->caption(), $this->failed_at->RequiredErrorMessage));
            }
        }
        if (!CheckDate($this->failed_at->FormValue, $this->failed_at->formatPattern())) {
            $this->failed_at->addErrorMessage($this->failed_at->getErrorMessage(false));
        }

        // Return validate result
        $validateForm = $validateForm && !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

    // Add record
    protected function addRow($rsold = null)
    {
        global $Language, $Security;

        // Set new row
        $rsnew = [];

        // uuid
        $this->uuid->setDbValueDef($rsnew, $this->uuid->CurrentValue, false);

        // connection
        $this->connection->setDbValueDef($rsnew, $this->connection->CurrentValue, false);

        // queue
        $this->queue->setDbValueDef($rsnew, $this->queue->CurrentValue, false);

        // payload
        $this->payload->setDbValueDef($rsnew, $this->payload->CurrentValue, false);

        // exception
        $this->exception->setDbValueDef($rsnew, $this->exception->CurrentValue, false);

        // failed_at
        $this->failed_at->setDbValueDef($rsnew, UnFormatDateTime($this->failed_at->CurrentValue, $this->failed_at->formatPattern()), false);

        // Update current values
        $this->setCurrentValues($rsnew);
        if ($this->uuid->CurrentValue != "") { // Check field with unique index
            $filter = "(`uuid` = '" . AdjustSql($this->uuid->CurrentValue, $this->Dbid) . "')";
            $rsChk = $this->loadRs($filter)->fetch();
            if ($rsChk !== false) {
                $idxErrMsg = str_replace("%f", $this->uuid->caption(), $Language->phrase("DupIndex"));
                $idxErrMsg = str_replace("%v", $this->uuid->CurrentValue, $idxErrMsg);
                $this->setFailureMessage($idxErrMsg);
                return false;
            }
        }
        $conn = $this->getConnection();

        // Load db values from old row
        $this->loadDbValues($rsold);

        // Call Row Inserting event
        $insertRow = $this->rowInserting($rsold, $rsnew);
        if ($insertRow) {
            $addRow = $this->insert($rsnew);
            if ($addRow) {
            } elseif (!EmptyValue($this->DbErrorMessage)) { // Show database error
                $this->setFailureMessage($this->DbErrorMessage);
            }
        } else {
            if ($this->getSuccessMessage() != "" || $this->getFailureMessage() != "") {
                // Use the message, do nothing
            } elseif ($this->CancelMessage != "") {
                $this->setFailureMessage($this->CancelMessage);
                $this->CancelMessage = "";
            } else {
                $this->setFailureMessage($Language->phrase("InsertCancelled"));
            }
            $addRow = false;
        }
        if ($addRow) {
            // Call Row Inserted event
            $this->rowInserted($rsold, $rsnew);
        }

        // Write JSON response
        if (IsJsonResponse() && $addRow) {
            $row = $this->getRecordsFromRecordset([$rsnew], true);
            $table = $this->TableVar;
            WriteJson(["success" => true, "action" => Config("API_ADD_ACTION"), $table => $row]);
        }
        return $addRow;
    }

